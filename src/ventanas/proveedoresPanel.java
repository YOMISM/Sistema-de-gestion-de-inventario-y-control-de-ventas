    private void cargarProveedores(){
        ProveedoresBD proveedores;
        ResultSet set;
        proveedores = new ProveedoresBD(conexion);
        set = proveedores.cargarProveedores();
        float precio;
        limpiarTablas();
        try{
            while(set.next()){
                modeloAgregar.addRow(new Object[]{set.getString(1), set.getString(2),
                set.getString(3), set.getString(4)});
            }
        }
        catch(Exception e){
            System.out.println(e.getMessage());
        }
    }
    
    private void limpiarTablas(){
        int cantidad =modeloAgregar.getRowCount()-1;  
        for(int i=cantidad;i>=0;i--){ 
            modeloAgregar.removeRow(i);
        }
    }  

