    private void agregarBotonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_agregarBotonActionPerformed
        System.out.println(rifTexto.getText()+nombreTexto.getText());
        if(!(rifTexto.getText().isEmpty() || nombreTexto.getText().isEmpty())){
            proveedorBD.SetProveedor(rifTexto.getText(), nombreTexto.getText(), direccionTexto.getText(), telefonoTexto.getText());
            modeloAgregar.addRow(new Object[]{rifTexto.getText(), nombreTexto.getText(),
            direccionTexto.getText(), telefonoTexto.getText()});
            rifTexto.setText("");
            nombreTexto.setText("");
            direccionTexto.setText("");
            telefonoTexto.setText("");
            JOptionPane.showMessageDialog(null, "Proveedor agregardo con éxito");
        }
        else{
            JOptionPane.showMessageDialog(null, "Ingrese los datos");
        }
    }//GEN-LAST:event_agregarBotonActionPerformed

    private void botonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonBuscarActionPerformed
        Proveedorees proveedor = proveedorBD.buscadorProveedor(buscadorTexto.getText());
        System.out.println(proveedor.getNombre());
        if(!(proveedor.getNombre() == null)){
            limpiarTablas();
             modeloAgregar.addRow(new Object[]{proveedor.getRif(), proveedor.getNombre(),
                    proveedor.getDireccion(), proveedor.getTelefono()});
            }
        else{
            JOptionPane.showMessageDialog(null, "Proveedor no encontrado");
        }
    }//GEN-LAST:event_botonBuscarActionPerformed

    private void buscadorTextoKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_buscadorTextoKeyPressed
        Proveedorees proveedor = proveedorBD.buscadorProveedor(buscadorTexto.getText());
        int key = evt.getKeyCode();
        boolean enter = key == 10;
        if ((enter)){
            if(!(proveedor.getNombre() == null)){
                limpiarTablas();
                modeloAgregar.addRow(new Object[]{proveedor.getRif(), proveedor.getNombre(),
                proveedor.getDireccion(), proveedor.getTelefono()});
            }
            else{
                JOptionPane.showMessageDialog(null, "Proveedor no encontrado");
            }

        }
    }//GEN-LAST:event_buscadorTextoKeyPressed

