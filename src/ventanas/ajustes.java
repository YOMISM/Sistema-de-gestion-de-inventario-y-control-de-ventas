    private void guardarBotonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_guardarBotonMouseClicked
        try{
            parametros.setNombre(nombreTexto.getText());
            parametros.setDireccion(direccionTexto.getText());
            parametros.setRif(rifTexto.getText());
            parametros.setTasa(Double.parseDouble(tasaTexto.getText()));
            parametros.setIva(Integer.parseInt(ivaTexto.getText()));
            parametros.setGanancia(Integer.parseInt(gananciaTexto.getText()));
            parametros.setActivo(activo);
            parametros.guardar();
            if(activo){
                errorTasaLabel.setVisible(!parametros.isErrorTasa());
            }

        }
        catch(IOException | NumberFormatException e){
            System.out.println(e);
        }
    }//GEN-LAST:event_guardarBotonMouseClicked

