    private void diaBotonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_diaBotonActionPerformed
        if(diaBoton.getBackground() == Color.red){
            if(fechaPC.equals(fechaSistema)){
                JOptionPane.showMessageDialog(null, "Este día ya ha sido cerrado");
            }
            else{
                diaBoton.setBackground(Color.green);
                diaBoton.setText("Iniciado");  
                diaLabel.setText("Iniciado");
                dia = true;
                parametros.setDia(dia);
                System.out.println(dia);
                fechaProgramaLabel.setText(fechaPcLabel.getText());
                ventanaPrincipal.setDateDiaLabel(fechaPC);
                ventanaPrincipal.iniciarDia(dia);
                fechaSistema = fechaPC;
                parametros.setFecha(fechaPC);
            }
        }
        else{
            if (fechaPC.equals(fechaSistema)) {
    int respuesta = JOptionPane.showConfirmDialog(
        null, 
        "Una vez finalizado el día no se podrá iniciar hasta mañana\n¿Aún desea finalizar el día?", 
        "Finalizar día", 
        JOptionPane.YES_NO_OPTION
    );
    
    // Si el usuario NO confirma (NO o Cierra el diálogo), salimos sin hacer nada
    if (respuesta != 0) {
        return; // o continuar con otro flujo si es necesario
    }
}

        diaBoton.setBackground(Color.red);
        diaBoton.setText("Iniciar"); 
        diaLabel.setText("Sin iniciar");
        errorTasaLabel.setVisible(false);
        dia = false;
        parametros.setDia(dia);
        ventanaPrincipal.iniciarDia(dia);
        }
        try {
            parametros.guardar();
        } catch (IOException ex) {
            Logger.getLogger(ajustes.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_diaBotonActionPerformed

